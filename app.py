def print_bytes(bytes, width, height, scale_w, scale_h):
    for y in range(height):
        for x in range(width):
            if bytes[y][x] != 0:
                oled.pixel(x*scale_w, y*scale_h, 1)

def calculate_bytes(source, width, height, target):
    for y in range(height):
        up = y-1 if y>0 else height-1
        down = y+1 if y<(height-1) else 0
        for x in range(width):
            left = x-1 if x>0 else width-1
            right = x+1 if x<(width-1) else 0
            score = \
                source[up][x] + source[up][left] + source[up][right] + \
                source[y][left] + source[y][right] + \
                source[down][x] + source[down][left] + source[down][right]

            if score > 3:
                target[y][x] = 0
            elif score == 3:
                target[y][x] = 1
            elif score == 2:
                target[y][x] = 1 if source[y][x] != 0 else 0
            else:
                target[y][x] = 0


SCALE = 2

def start():
    oled.fill(0)
    oled.flip()
    print_bytes(BYTES, OLED_WIDTH//SCALE, OLED_HEIGHT//SCALE, SCALE, SCALE)
    oled.show()

    buffer = [[0 for _ in range(OLED_WIDTH//SCALE)] for _ in range(OLED_HEIGHT//SCALE)]
    input_buffer = BYTES
    output_buffer = buffer
    while True:
        calculate_bytes(input_buffer, OLED_WIDTH//SCALE, OLED_HEIGHT//SCALE, output_buffer)
        oled.fill(0)
        print_bytes(output_buffer, OLED_WIDTH//SCALE, OLED_HEIGHT//SCALE, SCALE, SCALE)
        oled.show()
        temp_buffer = input_buffer
        input_buffer = output_buffer
        output_buffer = temp_buffer

if __name__ == "__main__":
    start()
